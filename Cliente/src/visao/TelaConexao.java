    private void btnConexaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConexaoActionPerformed
        try {
            Cliente cliente = new Cliente();
            int porta = Integer.parseInt(tfPortaConexao.getText());
            String host = tfEndConexao.getText();
            cliente.conectarCliente(host, porta);
            this.setVisible(false);
            TelaJogo tc = new TelaJogo(cliente);
            tc.setVisible(true);

            Mensagem m;
            m = tc.controle.receberMensagem();
            if (tc.controle.getId().equalsIgnoreCase("0")) {
                tc.setTitle("Primeiro Jogador");
                tc.primeiroJogador(m);

            } else {
                tc.setTitle("Segundo Jogador");
                tc.segundoJogador(m);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_btnConexaoActionPerformed

